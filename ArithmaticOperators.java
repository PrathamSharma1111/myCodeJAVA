public class ArithmaticOperators {
    public static void main(String[] args){
        int a = 10, b = 7;
        System.out.println(a+b);        //17
        System.out.println(a-b);        //3
        System.out.println(a*b);        //70
        System.out.println(a/b);        //1
        System.out.println(a%b);        //3
    }   
}
